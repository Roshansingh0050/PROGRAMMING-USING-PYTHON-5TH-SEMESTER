# Explain the difference between the following data types: string, list, tuple and set

# Tuple : Tuple is an ordered collection of elements enclosed with ()
# Tuples are immutable means once you declared a tuple then you cannot change the elements after declaration.
# example :
tup1 = (1,"roshan",3.4,True)
print(type(tup1))
print(tup1)




# List : List is an ordered collection of elements enclosed with [].
# List are mutable means you can apply changes after the declaration.....
# example :
l1 = [1,"roshan",3.4,True]
print(type(l1))
print(l1)




# Set : Set is an unordered and unindexed collection of elements enclosed with {}
# Duplicates are not allowed in the set.....
# examlple :
s1 = {1,"roshan",True}
print(type(s1))
print(s1)



